class Calculadora {
  num somar(num number1, num number2) {
    return number1 + number2;
  }

  num subtrair(num number1, num number2) {
    return number1 - number2;
  }

  num dividir(num number1, num number2) {
    return number1 / number2;
  }

  num multiplicar(num number1, num number2) {
    return number1 * number2;
  }
}
